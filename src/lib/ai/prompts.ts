export function chapterByIndex(index: number) {
  return CHAPTERS.find((c) => c.index === index);
}

export function getQuestionsForChapter(chapterIndex: number): Question[] {
  return QUESTIONS.filter((q) => q.chapterIndex === chapterIndex);
}

/**
 * 한 챕터 분량 본문을 만들기 위한 사용자 프롬프트.
 *
 * style:  'simple' = 담백하게, 'rich' = 풍성하게
 * mode:   인터뷰 방식 (self/other).
 *         self(부모님 본인 자서전)는 person 값과 무관하게 항상 1인칭으로 강제.
 * person: other 모드에서만 적용. 'first' = 1인칭 (나는…), 'third' = 3인칭 (어머니는…).
 *         미지정 시 other는 3인칭.
 * historicalContext: Ch.6 전용, 체크한 시대적 경험 요약
 */
export function buildChapterPrompt(args: {
  toLabel: string;
  chapterIndex: number;
  answers: Record<string, string>;
  style?: "simple" | "rich";
  person?: "first" | "third";
  mode?: "self" | "other";
  historicalContext?: string;
}): string {
  const chapter = chapterByIndex(args.chapterIndex);
  if (!chapter) throw new Error(`Unknown chapter ${args.chapterIndex}`);

  const questions = getQuestionsForChapter(args.chapterIndex);
  const filled = questions
    .map((q) => ({ q, a: (args.answers[String(q.id)] ?? "").trim() }))
    .filter((x) => x.a.length > 0);

  if (filled.length === 0) {
    return `[${chapter.index}장 — ${chapter.name}] 챕터에 대해서는 ${args.toLabel}의 답변이 없습니다.

이 챕터는 매우 짧게 처리해 주세요:
- 한두 문장으로, "${args.toLabel}이 이 시기에 대해서는 자세한 이야기를 들려주지 않았다" 정도의 자연스러운 표현.
- 출력 형식: \`## ${chapter.index}장 — ${chapter.name}\` 헤딩 + 한두 문장 본문.`;
  }

  // self 모드에서는 부모님이 본 질문(textSelf)을 그대로 전달.
  const lines = filled
    .map(
      (x, i) => `### 답변 ${i + 1} (Q${x.q.id})

질문: ${((args.mode === "self" ? x.q.textSelf : x.q.textOther) ?? "").replace(/\n/g, " ")}

${args.toLabel}의 답변:
"""
${x.a}
"""`,
    )
    .join("\n\n---\n\n");

  // self 모드(부모님 본인의 자서전)는 항상 1인칭으로 강제한다.
  // person 필드가 'third'(스키마 기본값)로 남아 있어도 "나은 ~했다" 같은
  // 어색한 3인칭이 나오지 않도록 보장. other 모드만 person 선택을 따른다.
  const person =
    args.mode === "self" ? "first" : (args.person ?? "third");
  const isFirst = person === "first";
  const perspectiveRule = isFirst
    ? `- **반드시 1인칭 시점으로만 작성하세요. 서술 주어는 언제나 '나'입니다 — "나는 ~했다", "내가 ~했다", "나의 ~" 형태로만 쓰세요.**
  - ${args.toLabel} 같은 호칭이나 이름을 주어로 쓰지 마세요. "${args.toLabel}은/는...", "그는...", "그녀는..." 같은 3인칭 서술은 절대 금지입니다.
  - 조사도 '나'에 맞추세요: "나는"(O), "나의"(O), "내가"(O) / "나은"(X), "나이"(X) 같은 틀린 조사 금지.
  - (이 1인칭 지시는 시스템 프롬프트의 3인칭 예시보다 절대적으로 우선합니다.)`
    : `- ${args.toLabel}을 3인칭으로 부르세요 ("${args.toLabel}은...", "${args.toLabel}의..."). '나'로 쓰지 마세요.`;

  const styleInstruction =
    args.style === "rich"
      ? `- 문체: 풍성하게 — 시대적 맥락, 감정의 흐름, 장면의 분위기를 더해 읽기 좋은 산문으로 풀어주세요. 단, 답변에 없는 사실을 만들지 마세요.`
      : `- 문체: 담백하게 — 답변 내용을 최대한 그대로 존중하고, 꾸밈 없이 담백한 산문으로 정리해주세요.`;

  const histCtx = args.historicalContext
    ? `\n참고 (시대적 경험): ${args.historicalContext}. 답변에 관련 내용이 있다면 적절히 맥락으로 활용해주세요 (없는 내용 추가 금지).`
    : "";

  return `다음은 [${chapter.index}장 — ${chapter.name}] 챕터를 위한 ${isFirst ? "나의" : `${args.toLabel}의`} 인터뷰 답변입니다.
이 답변들을 바탕으로 책의 ${chapter.index}장 본문을 작성해 주세요.

답변자 호칭: ${args.toLabel}
챕터 주제: ${chapter.description}
답변 개수: ${filled.length}개${histCtx}

---

${lines}

---

위 답변들을 바탕으로 ${chapter.index}장 본문을 작성해 주세요.

작성 규칙 (시스템 프롬프트의 절대 원칙을 우선 준수):
${perspectiveRule}
${styleInstruction}
- 답변 순서가 아니라 자연스러운 시간 흐름·주제 흐름으로 단락을 배치하세요.
- 답변에 있는 표현·비유·사투리는 가능한 한 그대로 살리세요.
- 답변에 없는 사실은 절대 만들지 마세요. 추측 표현, 시적 미화 금지 (rich 스타일이어도 사실 왜곡 불가).
- 본문은 4~7개 단락, 단락당 200~400자.
- 출력은 Markdown — \`## ${chapter.index}장 — ${chapter.name}\`로 시작해서 본문 단락으로 마무리.
- 챕터 시작 직후, 답변 중 인상적인 짧은 인용을 \`> "..."\` 형태로 한 줄 넣을 수 있습니다 (선택).
- 코드블록(\`\`\`)이나 추가 메타 설명 없이 본문만 출력.`;
}

/**
 * 책의 제목과 표지 부제를 만드는 프롬프트.
 * 출력은 JSON: { title, subtitle }
 */
export function buildTitlePrompt(args: {
  toLabel: string;
  answers: Record<string, string>;
}): string {
  const samples = Object.entries(args.answers)
    .filter(([, v]) => typeof v === "string" && v.trim().length > 0)
    .slice(0, 8)
    .map(([k, v]) => `Q${k}: ${v.trim().slice(0, 200)}`)
    .join("\n\n");

  return `${args.toLabel}의 자서전 표지에 들어갈 제목과 부제를 만들어 주세요.

답변 일부:
${samples}

요구사항:
- **title (제목)**: 5~12자. ${args.toLabel}을 포함하거나 호칭 없이도 OK.
  담백한 톤. 시적이지 않아야 합니다.
  좋은 예: "${args.toLabel}의 한 권" / "${args.toLabel}의 시간" / "${args.toLabel}이 살아온 길"
  나쁜 예: "별빛 아래 ${args.toLabel}" / "영원한 ${args.toLabel}의 노래"
- **subtitle (부제)**: 한 줄, 12~22자. 책의 정조를 잡는 한 문장.
  좋은 예: "한권이 정성껏 엮은 ${args.toLabel}의 이야기"
  나쁜 예: "시간을 거슬러 만나는 ${args.toLabel}의 영혼"
- 추측이나 미사여구 금지.
- 답변에 없는 표현은 만들지 마세요.

JSON으로만 출력하세요. 다른 텍스트나 코드블록 없이:
{
  "title": "...",
  "subtitle": "..."
}`;
}

/**
 * 책 첫 장 헌사 페이지 — 부모님이 자녀/가족에게 보내는 짧은 헌사.
 * Ch7 답변(Q38 "자녀에게 차마 못 한 이야기", 보조 Q40)에서
 * 자녀에게 전하는 메시지를 다듬어 만듭니다.
 */
export function buildDedicationPrompt(args: {
  toLabel: string;
  closingAnswers: Record<string, string>;
}): string {
  const closingText = Object.entries(args.closingAnswers)
    .filter(([, v]) => typeof v === "string" && v.trim().length > 0)
    .map(([k, v]) => `(${k}) ${v.trim()}`)
    .join("\n\n");

  if (!closingText) {
    return `${args.toLabel}이 자녀나 가족에게 직접 전하는 메시지가 답변에 명시되지 않았습니다.
짧은 일반 헌사를 한 줄로 만들어 주세요.
- "이 책을 사랑하는 가족에게" 정도의 담백한 한 줄.
- 본문만 출력 (인용부호나 메타 설명 없이).`;
  }

  return `${args.toLabel}이 자녀에게 남기는 짧은 헌사를 책 첫 장에 넣고 싶어요.
부모님 답변에서 자녀에게 전하는 메시지를 추출해, 짧게 다듬어 주세요.

부모님 답변:
${closingText}

요구사항:
- **${args.toLabel}의 1인칭** ("내가...", "너에게...", "사랑하는 ~에게" 같은 톤).
- 2~4줄, 한 줄에 8~16자.
- 시적 미화 금지. 부모님이 직접 자녀에게 말하는 톤.
- 답변에 있는 표현을 가능한 한 보존하세요.
- 답변에 없는 표현은 만들지 마세요.

본문만 출력 (인용부호나 메타 설명 없이, 줄바꿈으로 구분).`;
}

/**
 * 책 마지막 페이지 closing 메시지.
 * Ch7 답변(Q41 "내 인생을 한 문장으로", 하위 티어는 Q39/Q40 fallback)을
 * 바탕으로 책을 닫는 한두 줄.
 */
export function buildClosingPrompt(args: {
  toLabel: string;
  reflectionAnswer?: string;
}): string {
  const reflection = args.reflectionAnswer?.trim();

  if (!reflection) {
    return `${args.toLabel}의 자서전 마지막 페이지를 닫는 짧은 한 줄을 만들어 주세요.

요구사항:
- 한 줄, 10~30자.
- "이 책은 ${args.toLabel}이 살아온 시간으로 만들어졌습니다." 정도의 담백한 마무리.
- 본문만 출력.`;
  }

  return `${args.toLabel}의 자서전 마지막 페이지에 들어갈 closing 한두 줄을 만들어 주세요.

${args.toLabel}이 인생을 돌아보며 남긴 답변:
"""
${reflection}
"""

요구사항:
- 한두 문장. 한 줄에 10~25자.
- ${args.toLabel}이 자신의 인생을 돌아보며 남기는 짧은 한마디.
  또는 책을 마무리하는 짧은 산문 한 줄.
- 답변에 있는 표현을 가능하면 살려 주세요.
- 시적 미화 금지.

본문만 출력 (메타 설명 없이).`;
}
